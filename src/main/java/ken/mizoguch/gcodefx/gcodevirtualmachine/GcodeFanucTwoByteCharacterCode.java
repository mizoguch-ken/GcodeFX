    static public String encode(String code) {
        StringBuilder result = new StringBuilder(), buffer = new StringBuilder();
        boolean isComment = false, isExComment = false, isCharacterCode = false;
        char chr;

        for (int i = 0; i < code.length(); i++) {
            chr = code.charAt(i);
            switch (chr) {
                case '(':
                    if (isComment) {
                        if (isExComment) {
                            // Error
                            return code;
                        }
                        isExComment = true;
                    } else {
                        isComment = true;
                    }
                    result.append(chr);
                    break;
                case ')':
                    if (isExComment) {
                        isExComment = false;
                    } else {
                        if (!isComment) {
                            // Error
                            return code;
                        }
                        isComment = false;
                    }
                    result.append(chr);
                    break;
                case '@':
                    if (isComment && !isExComment) {
                        isCharacterCode = !isCharacterCode;
                        if (buffer.length() > 0) {
                            result.append(encoder(buffer));
                            buffer.delete(0, buffer.length());
                        }
                    } else {
                        // Error
                        return code;
                    }
                    break;
                default:
                    if (isCharacterCode && !isExComment) {
                        buffer.append(chr);
                    } else {
                        result.append(chr);
                    }
                    break;
            }
        }
        if (!isComment && !isExComment && !isCharacterCode) {
            return result.toString();
        } else {
            // Error
            return code;
        }
    }

    static private String encoder(StringBuilder sb) {
        if ((sb.length() == 0) || ((sb.length() % 2) == 1)) {
            return "@" + sb.toString() + "@";
        }

        try {
            StringBuilder result = new StringBuilder(), buffer = new StringBuilder();
            boolean next;
            int hexCode, num = 0, cnt = 0;
            for (next = true; next; cnt += num) {
                next = false;

                if (cnt < sb.length()) {
                    num = 4 - buffer.length();
                    if ((sb.length() - cnt) < num) {
                        num = sb.length() - cnt;
                    }
                    buffer.append(sb.substring(cnt, cnt + num));
                    if ((cnt + num) < sb.length()) {
                        next = true;
                    }
                }

                hexCode = Integer.parseInt(buffer.toString(), 16);
                if (CODE.containsKey(hexCode)) {
                    result.append(CODE.get(hexCode));
                    buffer.delete(0, buffer.length());
                } else {
                    hexCode = Integer.parseInt(buffer.substring(0, 2), 16);
                    if (CODE.containsKey(hexCode)) {
                        result.append(CODE.get(hexCode));
                    } else {
                        result.append("@");
                        result.append(buffer.substring(0, 2));
                        result.append("@");
                    }
                    buffer.delete(0, 2);
                }

                if (!next && (buffer.length() > 0)) {
                    next = true;
                }
            }
            return result.toString();
        } catch (NumberFormatException ex) {
            Console.writeStackTrace(GcodeInterpreterEnums.GCODE_FANUC_TWO_BYTE_CHARACTER_CODE.toString(), ex);
        }
        return "@" + sb.toString() + "@";
    }
